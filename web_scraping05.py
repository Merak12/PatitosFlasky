# En base a la salida del ejemplo anterior, se accede a cada campo de cada trabajo publicado
# <h2> para el título, <h3> para la compañía y <p> para la ubicación
# Cada uno de los tres campos aún se encuentra rodeado de elementos html

import requests
from bs4 import BeautifulSoup

URL = "https://realpython.github.io/fake-jobs/"
page = requests.get(URL)

soup = BeautifulSoup(page.content, "html.parser")
results = soup.find(id="ResultsContainer")
job_elements = results.find_all("div", class_="card-content")

for job_element in job_elements:
    title_element = job_element.find("h2", class_="title")
    company_element = job_element.find("h3", class_="company")
    location_element = job_element.find("p", class_="location")
    print(title_element)
    print(company_element)
    print(location_element)
    print()